
public class Ex03_실수형 {
	public static void main(String[] args) {
		double f1;
		f1 = 1.234;
		System.out.println("f1=" + f1);
		
		float f2 = -0.0987f;
		System.out.println("f2=" + f2);
		
		float f3 = 29;
		System.out.println("f3=" + f3);

		int f4 = (int)3.7;
		System.out.println("f4=" + f4);
		
		int f5 = (int)f3;
		System.out.println("f5=" + f5);
	}
}

